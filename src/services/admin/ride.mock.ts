// --- Ride detail mock data ---
export function buildMockRideDetail(ride: RideListItem): RideDetail {
  const stops: RideStop[] = [
    {
      id: `stop-${ride.id}-1`,
      address: ride.pickupAddress,
      latitude: ride.pickupLat,
      longitude: ride.pickupLng,
      stopOrder: 0,
      arrivedAt: ride.arrivedAt,
    },
    {
      id: `stop-${ride.id}-2`,
      address: ride.dropoffAddress,
      latitude: ride.dropoffLat,
      longitude: ride.dropoffLng,
      stopOrder: 1,
      arrivedAt: ride.completedAt,
    },
  ];

  const hasPayment = ride.actualFare !== null || ride.status === RideStatus.IN_PROGRESS;

  return {
    ...ride,
    user: { id: ride.userId, ...ride.user },
    driver: ride.driver
      ? { id: ride.driverId!, ...ride.driver }
      : null,
    stops,
    cancellation:
      ride.status === RideStatus.CANCELLED
        ? {
            cancelledBy: Role.USER,
            reason: 'User cancelled before pickup',
            fee: null,
            cancelledAt: ride.requestedAt,
          }
        : null,
    payment: hasPayment
      ? {
          id: `pay-${ride.id}`,
          amount: ride.actualFare ?? ride.estimatedFare,
          method: ride.paymentMethod,
          status: ride.completedAt ? PaymentStatus.COMPLETED : PaymentStatus.PENDING,
          refunds: [],
        }
      : null,
    ratings:
      ride.completedAt
        ? [{ rating: 4.8, comment: 'Great ride!' }]
        : [],
    riderRatings:
      ride.completedAt
        ? [{ rating: 4, comment: 'Friendly passenger' }]
        : [],
    destinationChanges:
      ride.id === 'ride-uuid-003'
        ? [{
            id: 'dc-001',
            oldDropoffAddress: 'Sliema Ferries',
            newDropoffAddress: 'Spinola Bay, St Julian\'s',
            oldEstimatedFare: 12.50,
            newEstimatedFare: 16.80,
            status: 'ACCEPTED' as const,
            createdAt: '2024-01-15T13:25:00Z',
          }]
        : [],
  };
}
